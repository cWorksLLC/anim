    f2l2 = "I- -I"
    f2l3 = "I ~ I"
    f2l4 = "I\_/I"
    f2l5 = "\___/"
    
    print(f2l1)
    print(f2l2)
    print(f2l3)
    print(f2l4)
    print(f2l5)
    
    time.sleep(0.25)
    